// o símbolo = tem o valor de atribuição
//a utilização do simbolo de (=) de atribuição juntamente com os operadores aritiméticos tem valores específicos.
// += (atribuição aditiva), -=)(atribuição subtrativa), *=(atribuição multiplicativa), /=(atribuição divisiva), %=(módulo ou resto)
let x = 10;
x = x + 6;
x = x - 1;

let y = 20;
y += 10;//y = y + 10;
y -= 2;//y = y - 2;
y *= 4;//y = y * 4;
y /= 2;//y = y / 2;
y %= 2;//y = y % 3;

//concatenação
let w = 'texto';
w += ', mais texto';
w += ' !!!'

console.log(x);
console.log(y);
console.log(w);